    private void databaseConnection() throws ClassNotFoundException, SQLException {

        //clear the exsting table
        
        removeAllrows(jTable1);
        
        
        Class.forName("com.mysql.jdbc.Driver");

        Connection con = DriverManager.getConnection(url, username, password);

        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery("select * from Staff");

        while (rs.next()) {
            String id = rs.getString(1);
            String name = rs.getString(2);
            String email = rs.getString(3);
            String join = rs.getString(4);
            String address = rs.getString(5);
            String phone = rs.getString(6);
            String salary = rs.getString(7);

            Object[] content = {id, name, email, phone, address, join, salary};
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(content);
        }
        rs.close();
        stmt.close();
        con.close();
    }

    private void removeAllrows(JTable table) {
        //remove all elements from table
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int rows = model.getRowCount();
        for (int i = rows - 1; i >= 0; i--) {
            model.removeRow(i);
        }

    }
