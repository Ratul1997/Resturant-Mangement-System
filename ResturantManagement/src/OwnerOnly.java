    private void addNewStaff() throws ClassNotFoundException, SQLException {

        Class.forName("com.mysql.jdbc.Driver");

        Connection con = DriverManager.getConnection(url, username, password);

        Statement stmt = con.createStatement();

        String querry = "INSERT INTO Staff (`Name`,`Email`,`Joining Date`,`Address`,`Phone Number`,`Salary`)"
                + "VALUES ('" + name.getText() + "','" + email.getText() + "','" + join.getText() + "', '" + address.getText() + "' , '" + phone.getText() + "', '"+salary.getText()+"')";
        stmt.executeUpdate(querry);

        stmt.close();
        con.close();

        databaseConnection();

    }
